do $$
declare
  v_secret text := encode(extensions.gen_random_bytes(32), 'hex');
begin
  insert into public.checkout_recovery_runtime (id, secret_hash)
  values (1, encode(extensions.digest(v_secret, 'sha256'), 'hex'))
  on conflict (id) do nothing;

  if not exists (select 1 from vault.secrets where name = 'checkout_recovery_secret') then
    perform vault.create_secret(
      v_secret,
      'checkout_recovery_secret',
      'Header interno da rotina de recuperacao de checkout'
    );
  end if;
end;
$$;

